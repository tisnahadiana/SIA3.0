package id.example.sisteminformasiakademik.admin.tnilaiAdmin;

public class TnilaiData {
    String nim, nama, prodi, semester, tnilai_url;

    public String getNim() {
        return nim;
    }

    public void setNim(String nim) {
        this.nim = nim;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getProdi() {
        return prodi;
    }

    public void setProdi(String prodi) {
        this.prodi = prodi;
    }

    public String getSemester() {
        return semester;
    }

    public void setSemester(String semester) {
        this.semester = semester;
    }

    public String getTnilai_url() {
        return tnilai_url;
    }

    public void setTnilai_url(String tnilai_url) {
        this.tnilai_url = tnilai_url;
    }
}
